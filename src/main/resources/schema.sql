DROP TABLE IF EXISTS users;
CREATE TABLE users ( Id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, username VARCHAR(100) NOT NULL, status VARCHAR(100));

DROP TABLE IF EXISTS groups;
CREATE TABLE groups ( Id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, title VARCHAR(100) NOT NULL);

DROP TABLE IF EXISTS message;
CREATE TABLE message ( Id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
content VARCHAR(100) NOT NULL,
status VARCHAR(100) NOT NULL,
timestamp long NOT NULL,
sender_id long NOT NULL,
receiver_id long,
group_id long);

DROP TABLE IF EXISTS user_contact;
CREATE TABLE user_contact ( user_id INT NOT NULL, contact_id INT NOT NULL);

DROP TABLE IF EXISTS user_group;
CREATE TABLE user_group ( user_id INT NOT NULL, group_id INT NOT NULL);